    chartOptions.series = [
        {name: series[0].name, data: series[0].data},
        {name: series[1].name, data: series[1].data}
    ];
  
    chartOptions.title.text = "Mock Data series size: " + series[0].data.length + " each";

    chartOptions.yaxis[0].min = 65;
